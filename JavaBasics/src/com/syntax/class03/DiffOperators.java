package com.syntax.class03;

public class DiffOperators {

	public static void main(String[] args) {
	
		int num = 100;
		num += 500;
		num -= 200;
		
	System.out.println(num);
	
	int num1 = 200;
	num1 -= 60;
	
	System.out.println(num1);
	
	int cakePiece = 11;
	cakePiece /= 4;
	
	System.out.println(cakePiece);
	
	int cake = 25;
	cake /= 7;
	System.out.println(cake);
		
		
	}

}
