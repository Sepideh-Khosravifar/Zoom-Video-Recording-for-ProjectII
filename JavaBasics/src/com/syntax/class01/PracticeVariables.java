package com.syntax.class01;

public class PracticeVariables {

	public static void main(String[] args) {
		
		// Create 4 numeric datatypes
		 
				byte shelf1=127;
				short shelf2=450;
				int shelf3=1000;
				long shelf4=2500;
				
				// Create 2 decimal datatypes 
				
				float account1=2250.30F;
				double account2=4500.01;
				
				// Create 2 character datatypes 
				char sign1='$';
				char sign2='@';
				
				// Create 2 boolean datatypes 
				
				boolean happy=true;
				boolean sad=false;
				
				// Print datatypes representing numeric values
				
				System.out.println(shelf1);
				System.out.println(shelf2);
				System.out.println(shelf3);
				System.out.println(shelf4);
				
				// Print datatypes representing decimal values
				
				System.out.println(account1);
				System.out.println(account2);
				
				//Print datatypes representing single character 
				
				System.out.println(sign1);
				System.out.println(sign2);
				
				//Print datatypes representing yes/no values
				
				System.out.println(happy);
				System.out.println(sad);
					

	}

}
